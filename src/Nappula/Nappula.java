package Nappula;

public class Nappula {

	private int x;
	private int y;
	private char id;
	
	
	public Nappula(int x, int y, char id){
		this.x = x;
		this.y = y;
		this.id = id;
	}
	
	
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	public char getId() {
		return id;
	}
	public void setX(int x) {
		this.x = x;
	}
	public void setY(int y) {
		this.y = y;
	}
	public void setId(char id) {
		this.id = id;
	}
	
	
	
	
}
